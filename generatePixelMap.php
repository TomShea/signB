function n($count) {
    $r = '';
    $e = '{-1,-1}, ';
    for($i = 0; $i < $count; $i++) {
        $r .= $e;
    }
    return $r;
}

function a($pin, $start, $end) {
    $r = '';
    if ($start > $end) {
        for($i = $start; $i >= $end; $i--) {
            $r .= '{' . "$pin, $i}, ";
        }
    } else {
        for($i = $start; $i <= $end; $i++) {
            $r .= '{' . "$pin, $i}, ";
        }
    }
    return $r;
}

function e(){
    return ' },' . PHP_EOL . '{';
}
